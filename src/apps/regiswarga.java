    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
    String wargaSql = "INSERT INTO warga "
                 + "(id_warga, nama_warga, jenis_kelamin, usia, nohp, nik, kk, alamat) "
                 + "VALUES (?,?,?,?,?,?,?,?)";
    String loginSql = "INSERT INTO login "
                 + "(id, nama, username,password, alamat, usia, jenis_kelamin, nohp, nik, kk,id_session, id_warga) "
                 + "VALUES (?,?,?,?,?,?,?,?,?,?,?,?)";

    try {
        PreparedStatement wargaStat = conn.prepareStatement(wargaSql);
        wargaStat.setString(1, id.getText());
        wargaStat.setString(2, nama.getText());
        wargaStat.setString(3, jk.getSelectedItem().toString());
        wargaStat.setString(4, usia.getText());
        wargaStat.setString(5, nohp.getText());
        wargaStat.setString(6, nik.getText());
        wargaStat.setString(7, kk.getText());
        wargaStat.setString(8, alamat.getText());

        PreparedStatement loginStat = conn.prepareStatement(loginSql);
        loginStat.setString(1, id.getText());
        loginStat.setString(2, nama.getText());
        loginStat.setString(3, nama.getText());
        loginStat.setString(4, "12345");
        loginStat.setString(5, alamat.getText());
        loginStat.setString(6, usia.getText());
        loginStat.setString(7, jk.getSelectedItem().toString());
        loginStat.setString(8, nohp.getText());
        loginStat.setString(9, nik.getText());
        loginStat.setString(10, kk.getText());
        loginStat.setString(11, "3");
        loginStat.setString(12, id.getText());

        conn.setAutoCommit(false); // Disable auto-commit to ensure both inserts are successful

        wargaStat.executeUpdate();
        loginStat.executeUpdate();

        conn.commit(); // Commit the changes to both tables

        JOptionPane.showMessageDialog(null, "Data akan disimpan");
        kosong();
        id.requestFocus();
        tabel();
    } catch (SQLException e) {
        try {
            conn.rollback(); // Rollback changes if an error occurs
        } catch (SQLException rollbackException) {
            // Handle rollback exception
        }
        JOptionPane.showMessageDialog(null, "Data gagal disimpan" + e);
        tabel();
    } finally {
        try {
            conn.setAutoCommit(true); // Re-enable auto-commit
        } catch (SQLException autoCommitException) {
            // Handle auto-commit exception
        }
    }
    }//GEN-LAST:event_saveActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null,"Hapus data ini?","Konfirmasi",JOptionPane.YES_NO_OPTION);
        if (ok==0){
            String sql = "delete from warga where nama_warga ='"+nama.getText()+"'";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data akan di hapus dari sistem?");
                kosong();
                id.requestFocus();
            }
            catch (SQLException e){
                JOptionPane.showMessageDialog(null, "data gagal dihapus"+e);
            }
            tabel();
        }
    }//GEN-LAST:event_deleteActionPerformed

