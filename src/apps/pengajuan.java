    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
         try {
            String sql = "INSERT INTO pengajuan "
                    + "(id, "
                    + "id_warga, "
                    + "nama_warga, "
                    + "kategori, "
                    + "masukan, "
                    + "status, "
                    + "tanggal_approve, "
                    + "tanggal_feedback, "
                    + "keluaran) "
                    + "VALUES (?,?,?,?,?,?,?,?,?)";

            // Pastikan koneksi database (conn) sudah dibuka sebelumnya

            try (PreparedStatement stat = conn.prepareStatement(sql)) {
                stat.setString(1, idpengajuan.getText());
                stat.setString(2, idwarga.getText());
                stat.setString(3, nawar.getText());
                stat.setString(4, kategori.getSelectedItem().toString());
                stat.setString(5, masukan.getText());
                stat.setString(6, "SEDANG DIPROSES");

                // Menggunakan java.util.Date untuk mendapatkan tanggal hari ini
                Date today = new Date(System.currentTimeMillis());
                // Menggunakan java.sql.Date untuk mengubah ke format yang sesuai dengan database
                stat.setDate(7, new java.sql.Date(today.getTime()));
                stat.setDate(8, new java.sql.Date(today.getTime()));

                stat.setString(9, "-");

                // Memeriksa apakah warga sudah melakukan pengajuan hari ini
                if (checkIfAlreadySubmittedToday()) {
                    JOptionPane.showMessageDialog(null, "Maaf, Anda hanya dapat melakukan pengajuan satu kali dalam sehari.");
                } else {
                    int rowsAffected = stat.executeUpdate();
                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                        kosong();
                        idpengajuan.requestFocus();
                        tabel();
                        kode();
                    } else {
                        JOptionPane.showMessageDialog(null, "Tidak ada baris yang terpengaruh");
                    }
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan: " + e.getMessage());
            tabel();
        }
    }//GEN-LAST:event_saveActionPerformed

