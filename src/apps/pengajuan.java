    public void kode() {
        try {
        
            String sql = "SELECT * FROM pengajuan ORDER by id desc";
            java.sql.Statement stat = conn.createStatement();
            ResultSet r = stat.executeQuery(sql);

            if (r.next()) {
                String nofak = r.getString("id").substring(1);
                String AN = "" + (Integer.parseInt(nofak) + 1);
                String Nol = "";

                if (AN.length() == 1) {
                    Nol = "000";
                } else if (AN.length() == 2) {
                    Nol = "00";
                } else if (AN.length() == 3) {
                    Nol = "0";
                } else if (AN.length() == 4) {
                    Nol = "";
                }

               idpengajuan.setText("P" + Nol + AN);
            } else {
               idpengajuan.setText("P0001");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }  
    
     private boolean checkIfAlreadySubmittedToday() throws SQLException {
        String today = new SimpleDateFormat("yyyy-MM-dd").format(new Date(System.currentTimeMillis()));

        String checkQuery = "SELECT COUNT(*) AS count FROM pengajuan WHERE id_warga = ? AND tanggal_approve = ?";

        try (PreparedStatement checkStat = conn.prepareStatement(checkQuery)) {
            checkStat.setString(1, idwarga.getText());
            checkStat.setString(2, today);

            try (ResultSet resultSet = checkStat.executeQuery()) {
                if (resultSet.next()) {
                    int count = resultSet.getInt("count");
                    return count > 0;
                }
            }
        }

        return false;
    }
    
