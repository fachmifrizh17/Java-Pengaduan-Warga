    public void tabel(){
        Object header[]={"ID PERMOHONAN","TANGGAL RESPON","NAMA WARGA","STATUS","MASUKAN","KETERANGAN"};
        DefaultTableModel data=new DefaultTableModel(null,header);
        tabel.setModel(data);
        setKoneksi();
        String sql = 
             "SELECT DISTINCT\n" +
             "a.id_approval,\n" +
             "a.id_pengajuan,\n" +
             "a.id_approv,\n" +
             "a.nama_approv,\n" +
             "a.tanggal,\n" +
             "a.nama_warga,\n" +
             "a.status,\n" +
             "a.masukan,\n" +
             "f.feedback\n" +
             "FROM approval a\n" +
             "LEFT JOIN feedback f ON f.id_pengajuan = a.id_pengajuan\n" +
             "WHERE a.nama_warga = ?;";
        try {
            PreparedStatement preparedStatement = conn.prepareStatement(sql);
            preparedStatement.setString(1, session.getU_username());  
            ResultSet rs = preparedStatement.executeQuery();
            while (rs.next())
            {
                String kolom1 = rs.getString("id_pengajuan");
                String kolom2 = rs.getString("tanggal");
                String kolom3 = rs.getString("nama_warga");
                String kolom4 = rs.getString("status");
                String kolom5 = rs.getString("masukan");
                String kolom6 = rs.getString("feedback");

                String kolom[]={kolom1,kolom2,kolom3,kolom4,kolom5,kolom6};
                data.addRow(kolom);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
        }
    }

